    public void calculateDescriptiveStatistics(double[] values) {

        // Calculate number of data, sum of data, max, min, mean, variance, RMS,
        // Standard deviation
        // The following calgorithms derive from
        // es.unex.sextante.math.simpleStats.SimpleStats
        // class
        for (final double dValue : values) {
            m_iCount++;
            final double dDelta = dValue - m_dMean;
            m_dMean = m_dMean + dDelta / m_iCount;
            m_dM2 = m_dM2 + dDelta * (dValue - m_dMean);
            m_dVariance = m_dM2 / (m_iCount - 1);
            m_dSum += dValue;
            m_dRMS += dValue * dValue;
            m_dMax = Math.max(m_dMax, dValue);
            m_dMin = Math.min(m_dMin, dValue);
            m_dStdDev = Math.sqrt(m_dVariance);
            m_dRMS = Math.sqrt(m_dRMS / m_iCount);
            if (counts.containsKey(dValue)) {
                counts.put(dValue, counts.get(dValue) + 1);
            } else {
                counts.put(dValue, 1);
                m_iClasses++;
            }
        }
        // Calculate Skewness and Kurtosis
        // algorithms derived from
        // https://www.spcforexcel.com/knowledge/basic-statistics/are-skewness-and-kurtosis-useful-statistics
        for (final double value : values) {
            m_dSkewn += Math.pow(value - m_dMean, 3)
                    / (m_iCount * Math.pow(m_dStdDev, 3));
            m_dKurt += Math.pow(value - m_dMean, 4)
                    / (m_iCount * Math.pow(m_dStdDev, 4));

        }

        // Calculate median, 25 and 75 percentiles
        // from
        // https://stackoverflow.com/questions/41413544/calculate-percentile-from-a-long-array
        Arrays.sort(values);
        final double pos1 = Math.floor((values.length - 1.0) / 2.0);
        final double pos2 = Math.ceil((values.length - 1.0) / 2.0);
        if (pos1 == pos2) {
            m_dMedian = values[(int) pos1];
        } else {
            m_dMedian = (values[(int) pos1] + values[(int) pos2]) / 2.0;
        }

        final int index25 = (int) Math.ceil(((double) 25 / (double) 100)
                * values.length);
        m_d25Percent = values[index25];
        final int index75 = (int) Math.ceil(((double) 75 / (double) 100)
                * values.length);
        m_d75Percent = values[index75];

    }

    /**
     * Returns the coefficient of variation of the array
     * 
     * @return variance/mean as double
     */
    public double getCoeffOfVar() {

        return m_dVariance / m_dMean;

    }

